"""Extract a per-dataset KPI inventory (signal FAMILIES) from normalized telemetry and
write it to configs/kpi_inventory.yaml, so the agent knows which KPIs exist (e.g. that
'diskio' exists for RCAeval components) instead of guessing signal names.

A "family" = a KPI name with component/node/hash identifiers stripped, so e.g.
  container.node-6.container_cpu_cfs_throttled_seconds  -> container_cpu_cfs_throttled_seconds
  ...UATWKR04_CpuPercent                                -> _CpuPercent
  trace.to_currencyservice.duration_p95                 -> trace.to_<svc>.duration_p95
"""
import re, sys, json
from collections import Counter
from pec_agent_planner_analyzer.tasks import load_task_bundles
from pec_agent_planner_analyzer.operators.base import long_frame

DATASETS = {
    "rcaeval_re2_ob": "configs/dataset_rcaeval_re2_ob.yaml",
    "rcaeval_re2_ss": "configs/dataset_rcaeval_re2_ss.yaml",
    "rcaeval_re2_tt": "configs/dataset_rcaeval_re2_tt.yaml",
    "openrca_bank": "configs/dataset_openrca_bank.yaml",
    "openrca_market_cb1": "configs/dataset_openrca_market_cb1.yaml",
    "openrca_market_cb2": "configs/dataset_openrca_market_cb2.yaml",
    "openrca_telecom": "configs/dataset_openrca_telecom.yaml",
}
HEX = re.compile(r"[0-9a-f]{8,}")
NODE = re.compile(r"node-\d+")
SVCWORD = re.compile(r"(to|from)_[a-z0-9]+", re.I)


def family(sig: str, components: list[str]) -> str:
    """Keep the REAL signal name; only strip the per-instance IDENTITY that makes the SAME KPI
    duplicate across components (host node prefix, docker hash, statefulset/pod index, the cmdb
    component id, mesh/trace edge service names). All metric-intrinsic tokens (latency-50, vda1,
    load_average_10s, CPU-0, eth0, status codes) are KEPT VERBATIM so the agent can READ them."""
    f = str(sig)
    # market container metrics carry the host: "container.node-6.<real_name>" -> "<real_name>"
    f = re.sub(r"^([a-z]+)\.node-\d+\.", r"\1.", f) if f.startswith("container.node-") else f
    f = re.sub(r"^container\.", "", f)            # leftover "container." prefix
    # bank container identity: Container-DOCKER_CONTAINER_<hash>--...--UATWKRxx_<Metric> -> Container_<Metric>
    f = re.sub(r"^Container-DOCKER_CONTAINER_[0-9a-f]+--.*?_([A-Za-z]+)$", r"Container_\1", f)
    # mesh edge: mesh.<dir>.<svcA>.<svcB>.<rest> -> mesh.<rest>  (the two service ids are identity)
    f = re.sub(r"^mesh\.(source|destination)\.[^.]+\.[^.]+\.", "mesh.", f)
    # trace edge service id: trace.to_currencyservice.x -> trace.to_SVC.x (keep marker, drop id)
    f = re.sub(r"\b(to|from)_[A-Za-z0-9]+", r"\1_SVC", f)
    # statefulset pod index is identity (the pod is a separate component)
    f = re.sub(r"statefulset-\d+", "statefulset", f)
    # strip any embedded cmdb component id
    for c in sorted([str(x) for x in components], key=len, reverse=True):
        if c and len(c) > 2 and c in f:
            f = f.replace(c, "").strip("-_. ")
    return f.strip("-_. ")


def main():
    out = {}
    for ds, cfg in DATASETS.items():
        bundles = load_task_bundles(dataset_config=cfg)
        comps = set()
        for b in bundles[:8]:
            comps |= set(str(c) for c in (b.task.component_inventory or []))
        comps = list(comps)
        fam = Counter()
        by_src = {}
        for b in bundles[:8]:   # union over a few tasks is enough; families are stable
            df = long_frame(b.task.task_dir)
            for src, sub in df.groupby(df["source"].astype(str)):
                s = by_src.setdefault(src, Counter())
                for sig in sub["signal"].astype(str).unique():
                    fa = family(sig, comps)
                    fam[fa] += 1; s[fa] += 1
        srcs = {src: sorted(c) for src, c in by_src.items()}
        print(f"{ds}: " + ", ".join(f"{src}={len(c)}" for src, c in srcs.items()))
        _append_to_config(DATASETS[ds], srcs)


MARK = "# --- kpi_inventory (auto-generated by scripts/extract_kpi_inventory.py) ---"


def _append_to_config(cfg_path: str, srcs: dict) -> None:
    """Append (or refresh) a `kpi_inventory:` block at the BOTTOM of the dataset config."""
    text = open(cfg_path, encoding="utf-8").read()
    # strip any previous block (from the marker to EOF) so re-running just refreshes it
    i = text.find(MARK)
    if i != -1:
        text = text[:i].rstrip() + "\n"
    block = [text.rstrip(), "", MARK,
             "# signal families per source (component/node/hash identifiers stripped) — tells the",
             "# agent which KPIs exist for this dataset, so it can READ them by name instead of guessing.",
             "kpi_inventory:"]
    for src, fams in srcs.items():
        block.append(f"  {src}:")
        for f in fams:
            block.append(f"  - {json.dumps(f, ensure_ascii=False)}" if False else f"    - {json.dumps(f, ensure_ascii=False)}")
    open(cfg_path, "w", encoding="utf-8").write("\n".join(block) + "\n")


if __name__ == "__main__":
    main()
